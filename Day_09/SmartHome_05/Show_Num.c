void Show_Num(int num1,int *py,int x,int y,int color)
{
    int i=WNum(num1);

    switch (i)
    {
        case 1:
        {
            Show_Word(num[num1],py,x,y,16,sizeof(num[num1]),color);
            break;
        }
        case 2:
        {
            //显示十位
            Show_Word(num[num1/10],py,x,y,16,sizeof(num[num1/10]),color);
            Show_Word(num[num1%10],py,x+18,y,16,sizeof(num[num1%10]),color);
            break;
        }
        case 3:
        {
            //显示百位
            Show_Word(num[num1/100],py,x,y,16,sizeof(num[num1/100]),color);
            //显示十位
            Show_Word(num[num1%100/10],py,x+18,y,16,sizeof(num[num1%100/10]),color);
            //显示个位
            Show_Word(num[num1%10],py,x+36,y,16,sizeof(num[num1%10]),color);
            break;

        }
        case 4:
        {
            //显示千位
            Show_Word(num[num1/1000],py,x,y,16,sizeof(num[num1/1000]),color);
            //显示百位
            Show_Word(num[num1/100%10],py,x+18,y,16,sizeof(num[num1/100%10]),color);
            //显示十位
            Show_Word(num[num1%100/10],py,x+36,y,16,sizeof(num[num1%100/10]),color);
            //显示个位
            Show_Word(num[num1%10],py,x+54,y,16,sizeof(num[num1%10]),color);

            break;
        }
        case 5:
        {
            //显示万位
            Show_Word(num[num1/10000],py,x,y,16,sizeof(num[num1/10000]),color);
            //显示千位
            Show_Word(num[num1/1000%10],py,x+18,y,16,sizeof(num[num1/1000%10]),color);
            //显示百位
            Show_Word(num[num1%1000/100],py,x+36,y,16,sizeof(num[num1%1000/100]),color);
            //显示十位
            Show_Word(num[num1%100/10],py,x+54,y,16,sizeof(num[num1%100/10]),color);
            //显示个位
            Show_Word(num[num1%10],py,x+72,y,16,sizeof(num[num1%10]),color);
            break;
        }
        case 6:
        {
            //显示十万位
            Show_Word(num[num1/100000],py,x,y,16,sizeof(num[num1/100000]),color);
            //显示万位
            Show_Word(num[num1/10000%10],py,x+18,y,16,sizeof(num[num1/10000%10]),color);
            //显示千位
            Show_Word(num[num1%10000/1000],py,x+36,y,16,sizeof(num[num1%10000/1000]),color);
            //显示百位
            Show_Word(num[num1%1000/100],py,x+54,y,16,sizeof(num[num1%1000/100]),color);
            //显示十位
            Show_Word(num[num1%100/10],py,x+72,y,16,sizeof(num[num1%100/10]),color);
            //显示个位
            Show_Word(num[num1%10],py,x+90,y,16,sizeof(num[num1%10]),color);
            break;
        }
        default:
            break;
    }
}

int WNum(int x)
{
    int y=0;
    while (x)
    {
        y+=1;
        x/=10;
    }
    return y;

}